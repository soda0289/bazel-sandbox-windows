// node_modules write stress: copy a real React + Angular node_modules tree
// (~18k files, deep scoped @angular/* dirs) wholesale into the overlay, then
// read a deep file back and enumerate. Every one of the thousands of writes
// must be redirected into the backing store - the real execroot stays empty.
//
// Run as: node node_modules_stress.js <node_modules_root> <execroot>
// Emits:  TOP=<top-level entries> COUNT=<file count> ANGVER=<@angular/core ver>
"use strict";
const fs = require("fs");
const p = require("path");

const src = p.join(process.argv[2], "node_modules");
const ws = process.argv[3];
const dest = p.join(ws, "node_modules");
fs.mkdirSync(dest, { recursive: true });

// Copy each top-level package (skip the pnpm store dir), dereferencing symlinks
// so real file contents are written into the overlay.
for (const e of fs.readdirSync(src)) {
    if (e === ".aspect_rules_js") continue;
    fs.cpSync(p.join(src, e), p.join(dest, e), { recursive: true, dereference: true });
}

const top = fs.readdirSync(dest).sort();

let n = 0;
(function walk(d) {
    for (const e of fs.readdirSync(d, { withFileTypes: true })) {
        const f = p.join(d, e.name);
        if (e.isDirectory()) walk(f);
        else n++;
    }
})(dest);

// Read a deep file back through the overlay to prove read-after-write.
const pkg = JSON.parse(fs.readFileSync(p.join(dest, "@angular", "core", "package.json"), "utf8"));

process.stdout.write("TOP=" + top.join(",") + " COUNT=" + n + " ANGVER=" + pkg.version);
